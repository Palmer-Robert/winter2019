public class Animal {
	public String type;
	public String name;
	public String dogTagId;
	
	
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDogTagId() {
		return dogTagId;
	}
	public void setDogTagId(String dogTagId) {
		this.dogTagId = dogTagId;
	}

}
